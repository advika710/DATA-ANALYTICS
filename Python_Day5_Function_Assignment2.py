#Q2 Declare a square() function with one parameter.Then call the function and pass one number and display the square of that number

# Define the function square with one parameter x
def square(x):
    # Calculate the square of x and return the result
    return x * x

# Call the function with one number and store the result
result = square(5)

# Print the square of the number
print("The square of the number is:", result)

"""
Output:
The square of the number is: 25
"""
