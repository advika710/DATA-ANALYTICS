#Q2. Compute the standard deviation of the NumPy array
#    Input: arr = [20, 2, 7, 1, 34]

import numpy as np  # Import the NumPy library

# Create a NumPy array with the given input
arr = np.array([20, 2, 7, 1, 34])

# Compute the standard deviation of the array
std_deviation = np.std(arr)

# Print the standard deviation
print("Standard deviation of the array:", std_deviation)

#ANS=> Input: arr = [20, 2, 7, 1, 34]
#ANS=> Standard deviation of the array: 12.576167937809991