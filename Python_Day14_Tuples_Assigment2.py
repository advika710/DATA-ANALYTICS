#Q2. Write a Python program to convert a list to a tuple.
#    Input: listx = [5, 10, 7, 4, 15, 3]

# Define the list with given elements
listx = [5, 10, 7, 4, 15, 3]

# Use the tuple() function to convert the list to a tuple
tuplex = tuple(listx)

# Print the result
print("The converted tuple is:", tuplex)

#ANS=> listx = [5, 10, 7, 4, 15, 3]
#ANS=> The converted tuple is: (5, 10, 7, 4, 15, 3)